package TotalPrice;



public class Produit {
	
	private int id;
	private String nom;
	private int prixuni;
	
	public Produit(int id, String nom, int prixuni) {
		super();
		this.id = id;
		this.nom = nom;
		this.prixuni = prixuni;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getPrixuni() {
		return prixuni;
	}
	public void setPrixuni(int prixuni) {
		this.prixuni = prixuni;
	}
	
	
	
	
	
	
	
	
	
	
	
	

}
